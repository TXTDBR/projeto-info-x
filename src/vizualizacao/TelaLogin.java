    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String login = txtLogin.getText().trim();
            String senha = (String) txtSenha.getText().trim();
                
            boolean retorno = new UsuarioDAO().logar(login, senha);
           // System.out.println(retorno);
            Usuario usuario = null;
            if (retorno) {
                usuario = new UsuarioDAO().getUsuarioPorLogin(login);
                String perfil = usuario.getPerfil();
                String user = usuario.getUsuario();
                if (perfil.equals("ADMINISTRADOR")) {
                    new TelaPrincipal().setVisible(true);
                    TelaPrincipal.lblUsuario.setText(user);
                    TelaPrincipal.menuRelatorio.setEnabled(true);
                    TelaPrincipal.itemCadUsuario.setEnabled(true);
                    TelaPrincipal.itemServico.setEnabled(true);
                    TelaPrincipal.lblUsuario.setForeground(Color.RED);
                    dispose();
                } else {
                    new TelaPrincipal().setVisible(true);
                    TelaPrincipal.lblUsuario.setText("Usu치rio: " +user);
                    dispose();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Senha ou usu치rio inv치lido(s)!");
                txtSenha.setText("");
                txtLogin.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

